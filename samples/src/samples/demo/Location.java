package samples.demo;

public class Location {
	private String name;
	private String address;
	
	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
	public String address() {
		return address;
	}
	
	public void setAddress(String address) {
		this.address = address;
	}
	
}
